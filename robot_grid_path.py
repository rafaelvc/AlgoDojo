# if grid[i][j] == 1 it is an obstacule
# moves are only right and dow
# Deque append complexity is O( 1 ) [ https://wiki.python.org/moin/TimeComplexity ]
def find_path(grid, robot_path, visited=None, cur_pos=(0,0)):
    if visited is None:
        visited = set()
    elif cur_pos in visited:
         return False
    # visited.add(cur_pos)
    r, c = cur_pos
    if len(grid)-1 == r and len(grid[0])-1 == c: # Path found
        robot_path.appendleft(cur_pos)
        return True
    # Next right is not out of grid or we do not have an obstacle 
    can_right = (r + 1 < len(grid) and grid[r+1][c] == 0)
    can_down = (c + 1 < len(grid[0]) and grid[r][c+1] == 0)
    if not can_right and not can_down:
        return False
    if can_right and find_path(grid, robot_path, visited, (r+1, c)):
        robot_path.appendleft(cur_pos)
        return True
    if can_down and find_path(grid, robot_path, visited, (r, c+1)): 
        robot_path.appendleft(cur_pos)
        return True
    visited.add(cur_pos)
    return False

